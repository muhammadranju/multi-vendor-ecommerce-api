// For more info on how mailgen content work visit https://github.com/eladnava/mailgen#readme
// Generate the plaintext version of the e-mail (for clients that do not support HTML)

// Initialize Mailgen instance for generating email templates
const mailGenerator = new Mailgen({
  theme: "default",
  product: {
    name: "Sole Mound",
    link: "https://github.com/muhammadranju/sole-mound-api",
  },
});

/**
 * Send email using nodemailer transporter
 * @param {Object} options - Options for sending email
 * @param {string} options.email - Recipient email address
 * @param {string} options.subject - Email subject
 * @param {Mailgen.Content} options.mailgenContent - Email content generated by Mailgen
 */
async function sendEmail(options) {
  const emailHtml = mailGenerator.generate(options.mailgenContent);
  const emailTextual = mailGenerator.generatePlaintext(options.mailgenContent);

  // Send email with defined transport object
  const info = await transporter.sendMail({
    from: `"Sole Mound ðŸ‘»"${config.USER_EMAIL}`, // sender address
    // from: `"Google"<mail.noreply@gmail.com>`, // sender address
    to: options.email, // Recipient(s) address
    subject: options.subject, /// Email subject
    text: emailTextual, // Plain text body
    html: emailHtml, // HTML body
  });

  console.log("Message successfully sent at: %s", info.messageId);
}

