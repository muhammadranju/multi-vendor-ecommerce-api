const Mailgen = require("mailgen");

const config = require("../config/config");

const transporter = nodemailer.createTransport({
  service: "gmail",
  port: 465,
  secure: true,
  auth: {
    user: config.EMAIL,
    pass: config.EMAIL_PASSKEY,
  },
});

